    private void BTNsalvarCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNsalvarCadActionPerformed
        // Obter as senhas digitadas
                String senha = new String(TXTsenhaCad.getPassword());
                String confirmaSenha = new String(TXTsenhaConfirma.getPassword());

                // Validar se as senhas sÃ£o iguais
              if (senha.equals(confirmaSenha)) {
                    // Se as senhas coincidirem, consulta o endereÃ§o via CEP
                    String cep = txtCEP.getText().replaceAll("[^0-9]", ""); // Limpa qualquer caractere nÃ£o numÃ©rico
                    if (cep.length() == 8) { // Verifica se o CEP tem 8 dÃ­gitos
                        CepUtil.consultarEnderecoPorCEP(cep, txtLogradouro, txtBairro, txtCidade, txtEstado);
                    } else {
                        JOptionPane.showMessageDialog(null, "CEP invÃ¡lido. Verifique o CEP e tente novamente.");
                    }
                } else {
                    // Se as senhas nÃ£o coincidirem
                    JOptionPane.showMessageDialog(null, "As senhas nÃ£o coincidem. Tente novamente.");
                }
            
    }//GEN-LAST:event_BTNsalvarCadActionPerformed

